/// <summary>
/// Enum defining the default UI text states (Edit, Save, Cancel).
/// </summary>
namespace SoftwareMeesters.Windows.UI.State
{
    /// <summary>
    /// Specifies the predefined text states that can be assigned to controls depending on application state.
    /// </summary>
    public enum ControlStateTexts
    {
        /// <summary>
        /// Indicates the control should display "Bewerken" (Edit).
        /// </summary>
        Edit = 0,

        /// <summary>
        /// Indicates the control should display "Opslaan" (Save).
        /// </summary>
        Save = 1,

        /// <summary>
        /// Indicates the control should display "Annuleren" (Cancel).
        /// </summary>
        Cancel = 2,
    }
}
